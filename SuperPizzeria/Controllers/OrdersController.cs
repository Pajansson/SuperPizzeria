        // GET: Orders
        public async Task<IActionResult> Index()
        {
            var applicationDbContext = _context.Orders.Include(o => o.Cart);
            return View(await applicationDbContext.ToListAsync());
        }


        // GET: Orders/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var order = await _context.Orders
                .Include(o => o.Cart)
                .SingleOrDefaultAsync(m => m.OrderId == id);
            if (order == null)
            {
                return NotFound();
            }

            return View(order);
        }

        public IActionResult Create()
        {
            var order = new Order
            {
                Cart = GetCurrentCart()
            };
            if (_signInManager.IsSignedIn(User))
            {
                var user = _userManager.GetCurrentUser(HttpContext);
                order.ApplicationUser = user.Result;
            }
            else
            {
                order.ApplicationUser = new ApplicationUser();
            }
                return View(order);
        }
        [HttpPost]
        public IActionResult Create(Order order)
        {
            var newOrder = new Order
            {
                ApplicationUser = order.ApplicationUser,
                Cart = order.Cart,
                CartId = order.CartId
            };
            _context.Orders.Add(newOrder);
            _context.SaveChanges();
            HttpContext.Session.Clear();
            return View("ThanksForOrder");
        }

        public Cart GetCurrentCart()
        {
            Cart cart = null;

            if (HttpContext.Session.GetString("Cart") != null)
            {
                var temp = HttpContext.Session.GetString("Cart");
                cart = JsonConvert.DeserializeObject<Cart>(temp);
            }

            return cart ?? new Cart();
